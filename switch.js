// alternative of if-else-----

switch ("color") {
    case "green":
        console.log("say green");
        break;
    case "black":
        console.log("say black");
        break;
    case "yellow":
        console.log("say yellow");
        break;
    case "red":
        console.log("say red");
        break;
    case "blue":
        console.log("say blue");
        break;
    default:
        console.log("say not available color");
}

